"""Benchmark DeepStream throughput across 1, 2, 4, 8, and 16 concurrent streams.

Runs ``deepstream-app`` for each auto-generated config in ``configs/benchmarks/``,
parses per-second FPS samples from DeepStream's ``**PERF:`` log lines, and
records system telemetry alongside throughput results.

Prerequisites:
    - DeepStream SDK installed with ``deepstream-app`` on PATH.
    - Config files pre-generated by ``scripts/generate_deepstream_configs.py``.

Usage::

    # 1. Generate configs first
    python scripts/generate_deepstream_configs.py --source-uri file:///path/to/video.mp4

    # 2. Run benchmarks
    python scripts/run_deepstream_benchmarks.py \\
        --config-dir configs/benchmarks \\
        --duration-s 90 \\
        --output-json results/deepstream_benchmarks.json \\
        --output-csv  results/deepstream_benchmarks.csv
"""

from __future__ import annotations

import argparse
import csv
import json
import os
import re
import signal
import subprocess
import sys
import time
from pathlib import Path
from typing import Dict, List, Optional

# Allow running from repo root without installing the package
sys.path.insert(0, str(Path(__file__).parent.parent))
from src.telemetry import TelemetryCollector


PERF_LINE_RE = re.compile(r"^\*\*PERF:\s+(.+)$", re.MULTILINE)
FPS_VAL_RE = re.compile(r"(\d+\.\d+)\s+\((\d+\.\d+)\)")


def parse_fps_samples(log_text: str) -> List[float]:
    """Extract per-second average FPS values from DeepStream log output.

    DeepStream emits lines like::

        **PERF:  29.97 (29.97)  30.01 (30.00)

    where each pair is ``current_fps (avg_fps)`` per stream.  This function
    averages across all streams per log line and returns one sample per line.

    Args:
        log_text: Full captured stdout from ``deepstream-app``.

    Returns:
        List of per-second average FPS values (one per ``**PERF:`` line).
    """
    samples: List[float] = []
    for m in PERF_LINE_RE.finditer(log_text):
        payload = m.group(1)
        if "FPS" in payload:
            continue  # skip header line
        vals = FPS_VAL_RE.findall(payload)
        if vals:
            avg_fps = sum(float(v[0]) for v in vals) / len(vals)
            samples.append(avg_fps)
    return samples


def run_deepstream_once(
    config_path: str,
    duration_s: int,
    warmup_samples: int,
) -> Dict[str, object]:
    """Launch deepstream-app for *duration_s* seconds and collect metrics.

    Args:
        config_path: Path to the DeepStream app config file.
        duration_s: How long to run (seconds) before sending SIGINT.
        warmup_samples: Number of initial FPS samples to discard.

    Returns:
        Dict with ``fps_avg``, ``fps_max``, ``fps_samples``, exit code,
        log tail, and telemetry summary fields.
    """
    cmd = ["deepstream-app", "-c", config_path]
    proc = subprocess.Popen(
        cmd,
        stdout=subprocess.PIPE,
        stderr=subprocess.STDOUT,
        text=True,
        bufsize=1,
    )

    telem = TelemetryCollector(interval_s=1.0)
    telem.start()

    lines: List[str] = []
    t0 = time.time()

    try:
        while True:
            if time.time() - t0 >= duration_s:
                break
            if proc.stdout is None:
                break
            line = proc.stdout.readline()
            if line:
                lines.append(line)
            elif proc.poll() is not None:
                break

        proc.send_signal(signal.SIGINT)
        try:
            proc.wait(timeout=10)
        except subprocess.TimeoutExpired:
            proc.kill()
            proc.wait(timeout=5)
    finally:
        summary = telem.stop().to_dict()

    fps_samples = parse_fps_samples("".join(lines))
    effective = fps_samples[warmup_samples:] if len(fps_samples) > warmup_samples else fps_samples
    fps_avg: Optional[float] = sum(effective) / len(effective) if effective else None
    fps_max: Optional[float] = max(effective) if effective else None

    result: Dict[str, object] = {
        "config": config_path,
        "exit_code": proc.returncode,
        "fps_samples": fps_samples,
        "fps_avg": fps_avg,
        "fps_max": fps_max,
        "log_tail": "".join(lines[-80:]),
    }
    result.update(summary)
    return result


def write_csv(rows: List[Dict[str, object]], output_csv: str) -> None:
    """Write benchmark rows to CSV, omitting list/log fields.

    Args:
        rows: List of result dicts from :func:`run_deepstream_once`.
        output_csv: Destination file path.
    """
    if not rows:
        return
    keys = sorted(k for k in rows[0] if k not in {"fps_samples", "log_tail"})
    with open(output_csv, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=keys)
        writer.writeheader()
        for row in rows:
            writer.writerow({k: row.get(k) for k in keys})


def discover_configs(config_dir: Path) -> List[Path]:
    """Find FP16 stream benchmark configs in *config_dir*.

    Args:
        config_dir: Directory to search (e.g. ``configs/benchmarks``).

    Returns:
        Sorted list of matching config file paths.
    """
    return sorted(config_dir.glob("deepstream_app_fp16_s*.txt"))


def measure_idle(duration_s: int = 10) -> Dict[str, object]:
    """Measure baseline system metrics with no active workload.

    Args:
        duration_s: How long to sample idle metrics (seconds).

    Returns:
        Telemetry summary dict with an additional ``label="idle"`` field.
    """
    print(f"\n[deepstream] measuring idle baseline for {duration_s}s ...")
    telem = TelemetryCollector(interval_s=1.0)
    telem.start()
    time.sleep(duration_s)
    summary = telem.stop().to_dict()
    summary["label"] = "idle"
    print(
        "[deepstream] idle: cpu={:.1f}%  ram={:.0f}MB  gpu_mem={:.0f}MB".format(
            summary.get("cpu_percent_avg") or 0,
            summary.get("ram_used_mb_avg") or 0,
            summary.get("gpu_memory_mb_avg") or 0,
        )
    )
    return summary


def main() -> None:
    """CLI entry point for DeepStream benchmark runner."""
    parser = argparse.ArgumentParser(
        description="DeepStream multi-stream benchmark runner",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    parser.add_argument("--config-dir", default="configs/benchmarks", help="Directory with benchmark configs")
    parser.add_argument("--duration-s", type=int, default=90, help="Runtime per config (seconds)")
    parser.add_argument("--warmup-samples", type=int, default=1, help="FPS samples to discard as warmup")
    parser.add_argument("--idle-s", type=int, default=10, help="Idle baseline measurement duration")
    parser.add_argument("--output-json", default="results/deepstream_benchmarks.json")
    parser.add_argument("--output-csv", default="results/deepstream_benchmarks.csv")
    args = parser.parse_args()

    config_dir = Path(args.config_dir)
    configs = discover_configs(config_dir)
    if not configs:
        raise FileNotFoundError(f"No FP16 benchmark configs found in {config_dir}. Run generate_deepstream_configs.py first.")

    idle = measure_idle(args.idle_s)

    rows: List[Dict[str, object]] = []
    for cfg in configs:
        print(f"\n[deepstream] running {cfg.name}")
        result = run_deepstream_once(str(cfg), duration_s=args.duration_s, warmup_samples=args.warmup_samples)

        m = re.search(r"_s(\d+)\.txt$", cfg.name)
        result["stream_count"] = int(m.group(1)) if m else None
        result["precision"] = "fp16"
        rows.append(result)

        fps_str = f"{result['fps_avg']:.2f}" if result["fps_avg"] is not None else "n/a"
        print(
            "[deepstream] fp16  streams={}  fps_avg={}  cpu={:.1f}%  ram={:.0f}MB  gpu_mem={:.0f}MB".format(
                result["stream_count"],
                fps_str,
                result.get("cpu_percent_avg") or 0,
                result.get("ram_used_mb_avg") or 0,
                result.get("gpu_memory_mb_avg") or 0,
            )
        )

    os.makedirs(os.path.dirname(args.output_json) or ".", exist_ok=True)
    output = {"idle_baseline": idle, "benchmarks": rows}
    with open(args.output_json, "w", encoding="utf-8") as f:
        json.dump(output, f, indent=2)
    write_csv(rows, args.output_csv)

    print(f"\nResults written to {args.output_json} and {args.output_csv}")


if __name__ == "__main__":
    main()
